public class NestedForLoops2 {

    public static void main(String[] args) {
        for (int j = 10; j >  0; j--) {
            int k = 1;
            if (k < 6) {
                System.out.print( k  + "\t");
                k++;
            }//while
             System.out.println();
        }//for j
    } //main
} //class
